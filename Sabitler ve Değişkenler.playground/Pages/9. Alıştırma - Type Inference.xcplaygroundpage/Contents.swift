 `katEdilenMesafe` adlı bir değişken tanımlayın ve değerini 0 olarak atayın.
 */
var katEdilenMesafe:Double = 0
/*:
 Şimdi ise `katEdilenMesafe` değişkenine 54.3 değerini atayın. Kodunuz düzgün çalışıyor mu? Çalışmıyorsa geri dönün ve kodun çalışabilmesi için `katEdilenMesafe` değerine doğru türü atayın.
 */
katEdilenMesafe = 54.3

