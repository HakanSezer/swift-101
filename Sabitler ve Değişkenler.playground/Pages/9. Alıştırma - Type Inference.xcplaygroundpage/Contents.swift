/*:
 ## Alıştırma - Type Inference
 
 `String` türünde `isim` adlı bir değişken tanımlayın ancak bir değer atamayın. Konsola `isim` değişkenini yazdırmayı deneyin. Kodunuz çalışıyor mu? Çalışmayan kod var ise bu kodları kaldırın.
 */
var isim:String

/*:
 Şimdi `isim` değişkenine bir değer atayın ve konsola yazdırın.
 */
isim = "Hakan"
print(isim)
