/*:
 ## Uygulama - Adım Sayısı
 
 Gün boyunca atılan adımların sayısını takip eden `adimlar` adlı bir değişken oluşturun. Başlangıçtaki adım sayısını temsil etmek için değişkenin değerini 0 olarak ayarlayın. Konsola `adimlar` değerini yazdırın.
 */
var adimlar = 0
print(adimlar)


/*:
 Kullanıcının yürüdüğünü varsayın. Güncel adım sayısını göstermek için `adimlar` değişkenini 2000 olarak güncelleyin ve konsola yazdırın. Kullanıcı hedefine ulaştığı için konsola "Günlük hedefine ulaşırken gayet iyi iş çıkardın!" yazdırın.
 */
adimlar += 2000
print(adimlar)
